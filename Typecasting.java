public class Typecasting {
    public static void main(String[] args) {

        char c = 'a';
        int ch = c ;
        System.out.println("Value of Char in int is :" + ch);

        int a = (int)(45.3333) ;
        System.out.println("integer value of a is :" + a);
        int i = 257;
        byte b = (byte)(i) ;
        System.out.println("Byte value of B is :" + b);



    }
}
